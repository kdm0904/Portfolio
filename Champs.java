package lol.champs;

public class Champs {
	private String champ;
	private int level;
	private double hp;
	private double hprecovery;
	private double resources;
	private double resourcesrecovery;
	private double attack;
	private double attackspeed;
	private double armour;
	private double magicarmour;
	private int range;
	private int speed;
	private int spell;
	
	public String getChamp() {
		return champ;
	}
	public void setChamp(String champ) {
		this.champ = champ;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public double getHp() {
		return hp;
	}
	public void setHp(double hp) {
		this.hp = hp;
	}
	public double getHprecovery() {
		return hprecovery;
	}
	public void setHprecovery(double hprecovery) {
		this.hprecovery = hprecovery;
	}
	public double getResources() {
		return resources;
	}
	public void setResources(double resources) {
		this.resources = resources;
	}
	public double getResourcesrecovery() {
		return resourcesrecovery;
	}
	public void setResourcesrecovery(double resourcesrecovery) {
		this.resourcesrecovery = resourcesrecovery;
	}
	public double getAttack() {
		return attack;
	}
	public void setAttack(double attack) {
		this.attack = attack;
	}
	public double getAttackspeed() {
		return attackspeed;
	}
	public void setAttackspeed(double attackspeed) {
		this.attackspeed = attackspeed;
	}
	public double getArmour() {
		return armour;
	}
	public void setArmour(double armour) {
		this.armour = armour;
	}
	public double getMagicarmour() {
		return magicarmour;
	}
	public void setMagicarmour(double magicarmour) {
		this.magicarmour = magicarmour;
	}
	public int getRange() {
		return range;
	}
	public void setRange(int range) {
		this.range = range;
	}
	public int getSpeed() {
		return speed;
	}
	public void setSpeed(int speed) {
		this.speed = speed;
	}
	public int getSpell() {
		return spell;
	}
	public void setSpell(int spell) {
		this.spell = spell;
	}
}
